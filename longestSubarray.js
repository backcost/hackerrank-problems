/* Finding longest subarray with absolute difference less than or 
equal to 1 */

function longestSubarray(arr) {
    var longest = 0
    
    for (let i1 = 0; i1 < arr.length; i1++) {
        var actualLength = 1
        var first = arr[i1]

        // finding the second number
        for (let i2 = i1 + 1; i2 < arr.length; i2++) {
            var second = null
            var theDifference = Math.abs(first - arr[i2])

            if (theDifference > 1) {
                break
            } else if (theDifference === 1) {
                second = arr[i2]
                break
            }
        }
        
        for (let i2 = i1 + 1; i2 < arr.length; i2++) {
            if (arr[i2] === first || arr[i2] === second) {
                actualLength++
            } else {
                break
            }
        }

        if (actualLength > longest) {
            longest = actualLength
            }
    }
    return longest
}

